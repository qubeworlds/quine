/// Strokes for a glyph (uppercased). Unknown chars render as blank (advance only).
pub fn glyph(c: u8) []const Seg {
    const up = if (c >= 'a' and c <= 'z') c - 32 else c;
    return switch (up) {
        'A' => &A, 'B' => &B, 'C' => &C, 'D' => &D, 'E' => &E, 'F' => &F, 'G' => &G,
        'H' => &H, 'I' => &I, 'J' => &J, 'K' => &K, 'L' => &L, 'M' => &M, 'N' => &N,
        'O' => &O, 'P' => &P, 'Q' => &Q, 'R' => &R, 'S' => &S, 'T' => &T, 'U' => &U,
        'V' => &V, 'W' => &W, 'X' => &X, 'Y' => &Y, 'Z' => &Z,
        '0' => &N0, '1' => &N1, '2' => &N2, '3' => &N3, '4' => &N4,
        '5' => &N5, '6' => &N6, '7' => &N7, '8' => &N8, '9' => &N9,
        '-' => &DASH, '.' => &DOT, ':' => &COLON, '/' => &SLASH,
        else => &NONE,
    };
}
