//! A tiny built-in single-stroke vector font — the data behind the `text` mesh
//! primitive. Each glyph is a list of straight segments in a unit cell
//! (x,y ∈ [0,1], origin bottom-left); the mesh builder (`assets.text`) extrudes
//! every segment into a thin oriented box. Curves are approximated by a few
//! straight segments — enough for labels (the drone's UP/DOWN, axis ticks, HUD
//! tags), not a typesetting engine. Uppercase A–Z, digits 0–9, space, and a few
//! punctuation marks; lowercase is uppercased by the builder.
//!
//! Keeping it here (plain data, no allocation, no GPU) means the same headless
//! `core` that generates a box can generate a word.

