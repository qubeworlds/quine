test "marching cubes meshes a sphere into a closed manifold near its surface" {
    var scene = SdfScene{};
    scene.add(.{ .prim = .sphere, .op = .union_op, .center = .{}, .radius = 1.0 });

    var mesh = try meshScene(testing.allocator, &scene, Vec3.splat(-1.6), Vec3.splat(1.6), 24);
    defer mesh.deinit(testing.allocator);

    try testing.expect(mesh.triangleCount() > 100);

    // Every generated vertex lies on the sphere surface (|distance| within the
    // grid step) — a strong check that the edge interpolation + tables are right.
    const step: f32 = 3.2 / 23.0;
    for (mesh.positions) |p| {
        try testing.expectApproxEqAbs(@as(f32, 0.0), scene.dist(p), 1.2 * step);
    }

    // Closed manifold: the sphere is fully inside the region, so the surface has
    // no boundary — every undirected edge must be shared by exactly two triangles.
    // A bad table row would leave cracks (edges used an odd number of times).
    const Edge = struct { a: u32, b: u32 };
    var counts = std.AutoHashMap(Edge, u32).init(testing.allocator);
    defer counts.deinit();
    var quantized = std.AutoHashMap([3]i32, u32).init(testing.allocator);
    defer quantized.deinit();

    // Weld vertices by quantized position so shared edges are recognised.
    const vid = try testing.allocator.alloc(u32, mesh.positions.len);
    defer testing.allocator.free(vid);
    var next_id: u32 = 0;
    for (mesh.positions, 0..) |p, i| {
        const key = [3]i32{
            @intFromFloat(@round(p.x * 2048.0)),
            @intFromFloat(@round(p.y * 2048.0)),
            @intFromFloat(@round(p.z * 2048.0)),
        };
        if (quantized.get(key)) |id| {
            vid[i] = id;
        } else {
            try quantized.put(key, next_id);
            vid[i] = next_id;
            next_id += 1;
        }
    }

    var t: usize = 0;
    while (t < mesh.indices.len) : (t += 3) {
        const v = [3]u32{ vid[mesh.indices[t]], vid[mesh.indices[t + 1]], vid[mesh.indices[t + 2]] };
        for (0..3) |k| {
            var a = v[k];
            var b = v[(k + 1) % 3];
            if (a > b) std.mem.swap(u32, &a, &b);
            const e = Edge{ .a = a, .b = b };
            const gop = try counts.getOrPut(e);
            if (gop.found_existing) gop.value_ptr.* += 1 else gop.value_ptr.* = 1;
        }
    }

    var it = counts.valueIterator();
    while (it.next()) |c| {
        try testing.expectEqual(@as(u32, 2), c.*); // every edge shared by exactly 2 tris
    }
}

